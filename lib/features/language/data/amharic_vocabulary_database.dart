  static List<AmharicWord> getWordsByCategory(String category) {
    return ComprehensiveAmharicData.getWordsByCategory(category);
  }

  static List<AmharicWord> getWordsByDifficulty(int difficulty) {
    return getAllWords().where((word) => word.difficulty == difficulty).toList();
  }

  static List<String> getAllCategories() {
    return ComprehensiveAmharicData.getAllCategories();
  }

  static AmharicWord? getWordById(int id) {
    try {
      return getAllWords().firstWhere((word) => word.id == id);
    } catch (e) {
      return null;
    }
  }

  static List<AmharicWord> searchWords(String query) {
    return ComprehensiveAmharicData.searchWords(query);
  }

